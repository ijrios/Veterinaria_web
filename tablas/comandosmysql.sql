-- ----------------------------
-- Table structure for users
-- ----------------------------
DROP TABLE IF EXISTS users;
CREATE TABLE users  (
    uid int(11) NOT NULL,
    uname varchar(30) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,password varchar(30) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    PRIMARY KEY (uid) USING BTREE
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for tipos_id
-- ----------------------------
DROP TABLE IF EXISTS tipos_id;
CREATE TABLE tipos_id  (
    tipo_id varchar(3) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    description varchar(30) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    PRIMARY KEY (tipo_id) USING BTREE
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for clientes
-- ----------------------------
DROP TABLE IF EXISTS clientes;
CREATE TABLE clientes  (
    id int(11) NOT NULL,
    tipo_id varchar(3) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    nombres varchar(50) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    apellidos varchar(50) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    fecha_ingreso date NULL DEFAULT NULL,
    direccion varchar(100) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT '',
    telefono varchar(15) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT '',
    PRIMARY KEY (id) USING BTREE,
    INDEX fk_clientes_01(tipo_id) USING BTREE,
    CONSTRAINT fk_clientes_01 FOREIGN KEY (tipo_id) REFERENCES tipos_id (tipo_id) ON DELETE RESTRICT ON UPDATE RESTRICT
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for proveedores
-- ----------------------------
DROP TABLE IF EXISTS proveedores;
CREATE TABLE proveedores  (
    id int(11) NOT NULL,
    tipo_id varchar(3) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    nombres varchar(50) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    apellidos varchar(50) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    fecha_ingreso date NULL DEFAULT NULL,
    direccion varchar(100) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    telefono varchar(15) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    PRIMARY KEY (id) USING BTREE,
    INDEX fk_proveedores_01(tipo_id) USING BTREE,
    CONSTRAINT fk_proveedores_01 FOREIGN KEY (tipo_id) REFERENCES tipos_id (tipo_id) ON DELETE RESTRICT ON UPDATE RESTRICT
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for productos
-- ----------------------------
DROP TABLE IF EXISTS productos;
CREATE TABLE productos  (
    id varchar(10) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    descripcion varchar(200) CHARACTER SET latin1 COLLATE latin1_general_cs NULL DEFAULT NULL,
    costo int(11) NULL DEFAULT NULL,
    precio int(11) NULL DEFAULT NULL,
    PRIMARY KEY (id) USING BTREE
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for productos_proveedores
-- ----------------------------
DROP TABLE IF EXISTS productos_proveedores;
CREATE TABLE productos_proveedores  (
    id_producto varchar(10) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    id_proveedor int(11) NOT NULL,
    PRIMARY KEY (id_producto, id_proveedor) USING BTREE,
    INDEX fk_pp_02(id_proveedor) USING BTREE,
    CONSTRAINT fk_pp_01 FOREIGN KEY (id_producto) REFERENCES productos (id) ON DELETE RESTRICT ON UPDATE RESTRICT,
    CONSTRAINT fk_pp_02 FOREIGN KEY (id_proveedor) REFERENCES proveedores (id) ON DELETE RESTRICT ON UPDATE RESTRICT
) ENGINE = InnoDB CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;

-- ----------------------------
-- Table structure for ventas
-- ----------------------------
DROP TABLE IF EXISTS ventas;
CREATE TABLE ventas  (
    id_pago int(11) NOT NULL AUTO_INCREMENT,
    id_cliente int(11) NOT NULL,
    fecha date NOT NULL,
    id_producto varchar(10) CHARACTER SET latin1 COLLATE latin1_general_cs NOT NULL,
    cantidad int(11) NOT NULL,
    PRIMARY KEY (id_pago) USING BTREE,
    INDEX fk_pagos_clientes_01(id_cliente) USING BTREE,
    INDEX fk_pagos_productos_01(id_producto) USING BTREE,
    CONSTRAINT fk_pagos_clientes_01 FOREIGN KEY (id_cliente) REFERENCES clientes (id) ON DELETE RESTRICT ON UPDATE RESTRICT,
    CONSTRAINT fk_pagos_productos_01 FOREIGN KEY (id_producto) REFERENCES productos (id) ON DELETE RESTRICT ON UPDATE RESTRICT
) ENGINE = InnoDB AUTO_INCREMENT = 15 CHARACTER SET = latin1 COLLATE = latin1_general_cs ROW_FORMAT = Dynamic;


